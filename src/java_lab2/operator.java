public class operator 
{
    public static void main(String a[])
    {
        int x=4,y=4;
        System.out.println(++x);
        System.out.println(++y);
        System.out.println(--x);
        System.out.println(--y);
        System.out.println(x/y);
        System.out.println(x*y);
        System.out.println(~x);
        System.out.println(x^y);
        System.out.println(x<y);
        System.out.println(x<=y);
        System.out.println(x|y);
    }
}
   
